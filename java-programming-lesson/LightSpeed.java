public class LightSpeed {
  public static void main(String[] args) {
    
    System.out.println("\"int\"light speed   : " + (300000 * 60 * 60 * 24) + " km per a day");
    System.out.println("\"long\"light speed  : " + (300000L * 60 * 60 * 24) + " km per a day");
    System.out.println("\"double\"light speed: " + (300000.0 * 60 * 60 * 24) + " km per a day");

  }
}
